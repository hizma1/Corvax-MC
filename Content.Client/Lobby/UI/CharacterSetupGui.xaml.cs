        private void UpdateRotation(float frameTime)
        {
            // CCM rework lobby - start
            if (_hoveredViews.Count == 0)
                return;

            foreach (var view in _hoveredViews)
            {
                _hoverRotationTimes[view] += frameTime;
                var progress = (_hoverRotationTimes[view] % 3f) / 3f;
                var direction = progress switch
                {
                    < 0.25f => Direction.South,
                    < 0.5f => Direction.West,
                    < 0.75f => Direction.North,
                    _ => Direction.East
                };

                view.OverrideDirection = direction;
            }
            // CCM rework lobby - end
        }

        private void HookPreviewRotation(PanelContainer container, SpriteView view, bool keepOrientationOnExit = false)
        {
            // CCM rework lobby - start
            container.OnMouseEntered += _ =>
            {
                _hoveredViews.Add(view);
                if (!keepOrientationOnExit || !_hoverRotationTimes.ContainsKey(view))
                {
                    _hoverRotationTimes[view] = 0f;
                    view.OverrideDirection = Direction.South;
                }
            };

            container.OnMouseExited += _ =>
            {
                _hoveredViews.Remove(view);
                if (!keepOrientationOnExit)
                {
                    _hoverRotationTimes[view] = 0f;
                    view.OverrideDirection = Direction.South;
                }
            };
            // CCM rework lobby - end
        }

        private void HandleDeletePressed()
        {
            // CCM rework lobby - start
            if (_carouselProfiles.Count == 0)
                return;

            if (_deleteReady)
            {
                DeleteCharacter?.Invoke(_carouselProfiles[_carouselIndex].Slot);
                ResetDeleteState();
                return;
            }

            _deleteCounting = true;
            _deleteCountdown = 3f;
            _deleteReady = false;
            var warning = new Color(1f, 0.7f, 0.7f, 1f);
            DeleteCharacterButton.Modulate = warning;
            UpdateDeleteText();
            // CCM rework lobby - end
        }

        private void UpdateDeleteCountdown(float frameTime)
        {
            // CCM rework lobby - start
            if (!_deleteCounting)
                return;

            _deleteCountdown -= frameTime;
            if (_deleteCountdown <= 0f)
            {
                _deleteCounting = false;
                _deleteReady = true;
                var confirm = new Color(1f, 0.3f, 0.3f, 1f);
                var text = Loc.GetString("character-setup-gui-delete-confirm");
                DeleteCharacterButton.Modulate = confirm;
                DeleteCharacterButton.Text = text;
                return;
            }

            UpdateDeleteText();
            // CCM rework lobby - end
        }

        private void UpdateDeleteText()
        {
            // CCM rework lobby - start
            var seconds = Math.Max(1, (int) MathF.Ceiling(_deleteCountdown));
            var text = Loc.GetString("character-setup-gui-delete-confirm-countdown", ("seconds", seconds));
            DeleteCharacterButton.Text = text;
            // CCM rework lobby - end
        }

        private void ResetDeleteState()
        {
            // CCM rework lobby - start
            _deleteCounting = false;
