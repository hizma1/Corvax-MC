        /// <summary>
        /// Disposes and reloads all character picker buttons from the preferences data.
        /// </summary>
        public void ReloadCharacterPickers()
        {
            // CCM rework lobby - start
            ResetDeleteState();
            _carouselProfiles.Clear();
            OldCharacters.DisposeAllChildren();
            if (!_preferencesManager.ServerDataLoaded)
                return;

            var selectedSlot = _preferencesManager.Preferences?.SelectedCharacterIndex;
            var numberOfFullSlots = 0;
            var characterButtonsGroup = new ButtonGroup();
            var allowedSpecies = new HashSet<string> { "Human", "Avali", "Arachnid", "Moth", "Felinid", "Dwarf" };
            foreach (var (slot, character) in _preferencesManager.Preferences!.Characters)
            {
                if (character is HumanoidCharacterProfile humanoidProfile &&
                    !allowedSpecies.Contains(humanoidProfile.Species.Id))
                {
                    continue;
                }

                _carouselProfiles.Add((slot, character));
                numberOfFullSlots++;

                var characterPickerButton = new CharacterPickerButton(_entManager,
                    _protomanager,
                    characterButtonsGroup,
                    character,
                    slot == selectedSlot);
                OldCharacters.AddChild(characterPickerButton);
                characterPickerButton.OnPressed += _ => SelectCharacter?.Invoke(slot);
                characterPickerButton.OnDeletePressed += () => DeleteCharacter?.Invoke(slot);
            }

            _carouselIndex = 0;
            if (selectedSlot != null)
            {
                var selectedIndex = _carouselProfiles.FindIndex(p => p.Slot == selectedSlot.Value);
                if (selectedIndex >= 0)
                    _carouselIndex = selectedIndex;
            }

            UpdateCarousel();

            var maxSlots = _preferencesManager.Settings?.MaxCharacterSlots ?? 0;
            var fullSlots = _carouselProfiles.Count;
            var canDeleteCharacter = fullSlots > 1;
            NewCharacterButton.Disabled = fullSlots >= maxSlots;
            OldNewCharacterButton.Disabled = numberOfFullSlots >= maxSlots;
            DeleteCharacterButton.Disabled = !canDeleteCharacter;
            OldDeleteCharacterButton.Disabled = false;
            // CCM rework lobby - end
        }

        public void UpdateLayoutBounds(Vector2 hostSize, Vector2 minSize, Vector2 maxSize)
        {
            _resizeHostSize = hostSize;
            _resizeMaxSize = new Vector2(
                MathF.Max(1f, MathF.Min(hostSize.X, maxSize.X)),
                MathF.Max(1f, MathF.Min(hostSize.Y, maxSize.Y)));
            _resizeMinSize = new Vector2(
                MathF.Min(minSize.X, _resizeMaxSize.X),
                MathF.Min(minSize.Y, _resizeMaxSize.Y));

            MinSize = _resizeMinSize;
            MaxSize = _resizeMaxSize;

            if (_hasManualSize)
                _manualSize = ClampSize(_manualSize);

            if (_hasManualPosition)
                _manualPosition = ClampPosition(_manualPosition, _hasManualSize ? _manualSize : Size);
        }

        public Vector2 MeasurePreferredSize(Vector2 availableSize)
        {
            var constrained = Vector2.Max(Vector2.One, availableSize);
            var previousMinSize = MinSize;
            var previousMaxSize = MaxSize;
            var previousSetSize = SetSize;
            var previousCompactTier = _compactTier;
            var previousResponsiveHeight = _lastResponsiveHeight;
            var previousCarouselMode = _carouselLayoutMode;
            var previousCarouselWidth = _lastCarouselWidth;

            try
            {
                ApplyCompactLayoutTier(ResolveCompactLayoutTier(constrained.Y));
                ApplyCarouselLayoutMode(ResolveCarouselLayoutMode(constrained.X));

                MinSize = Vector2.Zero;
                MaxSize = constrained;
                SetSize = new Vector2(float.NaN, float.NaN);
                Measure(constrained);
                return Vector2.Min(DesiredSize, constrained);
            }
            finally
            {
                MinSize = previousMinSize;
                MaxSize = previousMaxSize;
                SetSize = previousSetSize;
                ApplyCompactLayoutTier(previousCompactTier);
                _lastResponsiveHeight = previousResponsiveHeight;
                ApplyCarouselLayoutMode(previousCarouselMode);
                _lastCarouselWidth = previousCarouselWidth;
            }
        }

        private void CreateCharacter()
        {
            var hadSelectedCharacter = _preferencesManager.Preferences?.TryGetSelectedCharacter(out _) == true;
            _preferencesManager.CreateCharacter(HumanoidCharacterProfile.Random());

            if (!hadSelectedCharacter &&
                _preferencesManager.Preferences is { } prefs &&
                prefs.TryGetSelectedCharacter(out _))
            {
                SelectCharacter?.Invoke(prefs.SelectedCharacterIndex);
                return;
            }

            ReloadCharacterPickers();
        }

        private void StepCarousel(int direction)
        {
            // CCM rework lobby - start
            if (_carouselProfiles.Count <= 1)
                return;

            _carouselIndex = (_carouselIndex + direction) % _carouselProfiles.Count;
            if (_carouselIndex < 0)
                _carouselIndex += _carouselProfiles.Count;

            var slot = _carouselProfiles[_carouselIndex].Slot;
            SelectCharacter?.Invoke(slot);
            // CCM rework lobby - end
        }

        private void PreviewClicked(GUIBoundKeyEventArgs args, int offset)
        {
            // CCM rework lobby - start
            if (args.Function != EngineKeyFunctions.UIClick)
                return;

            if (_carouselProfiles.Count <= 1)
                return;

            if (Math.Abs(offset) == 2 && _carouselProfiles.Count < 4)
                return;

            var targetIndex = (_carouselIndex + offset) % _carouselProfiles.Count;
            if (targetIndex < 0)
                targetIndex += _carouselProfiles.Count;

            _carouselIndex = targetIndex;
            SelectCharacter?.Invoke(_carouselProfiles[_carouselIndex].Slot);
            UpdateCarousel();
            args.Handle();
            // CCM rework lobby - end
        }

        private void UpdateCarousel()
        {
            // CCM rework lobby - start
            DeletePreviewEntities();
            _carouselLayoutMode = ResolveCarouselLayoutMode(CarouselPanel.Size.X);
            ApplyCarouselLayoutMode(_carouselLayoutMode);

            if (_carouselProfiles.Count == 0)
            {
                SetPreview(LeftFarPreview, LeftFarNameLabel, null, default);
                SetPreview(LeftPreview, LeftNameLabel, null, default);
                SetPreview(CenterPreview, CenterNameLabel, null, default);
                SetPreview(RightPreview, RightNameLabel, null, default);
                SetPreview(RightFarPreview, RightFarNameLabel, null, default);
                LeftPreviewContainer.Visible = false;
                RightPreviewContainer.Visible = false;
                LeftFarPreviewContainer.Visible = false;
                RightFarPreviewContainer.Visible = false;
                return;
            }

            var center = _carouselProfiles[_carouselIndex];
            _centerPreviewEntity = CreatePreviewEntity(center.Profile, _showClothes);
            SetPreview(CenterPreview, CenterNameLabel, center.Profile, _centerPreviewEntity);
            if (center.Profile is HumanoidCharacterProfile humanoidCenter)
                _centerPreviewSpeciesId = humanoidCenter.Species;
            else
                _centerPreviewSpeciesId = SharedHumanoidAppearanceSystem.DefaultSpecies;

            if (_carouselProfiles.Count == 1 || _carouselLayoutMode == CarouselLayoutMode.One)
            {
                LeftPreviewContainer.Visible = false;
                RightPreviewContainer.Visible = false;
                LeftFarPreviewContainer.Visible = false;
                RightFarPreviewContainer.Visible = false;
                return;
            }

            LeftPreviewContainer.Visible = true;
            RightPreviewContainer.Visible = true;
            LeftFarPreviewContainer.Visible = _carouselLayoutMode == CarouselLayoutMode.Five && _carouselProfiles.Count >= 4;
            RightFarPreviewContainer.Visible = _carouselLayoutMode == CarouselLayoutMode.Five && _carouselProfiles.Count >= 4;

            var leftIndex = (_carouselIndex - 1 + _carouselProfiles.Count) % _carouselProfiles.Count;
            var rightIndex = (_carouselIndex + 1) % _carouselProfiles.Count;
            var leftFarIndex = (_carouselIndex - 2 + _carouselProfiles.Count) % _carouselProfiles.Count;
            var rightFarIndex = (_carouselIndex + 2) % _carouselProfiles.Count;

            var left = _carouselProfiles[leftIndex];
            var right = _carouselProfiles[rightIndex];
            var leftFar = _carouselProfiles[leftFarIndex];
            var rightFar = _carouselProfiles[rightFarIndex];

            _leftPreviewEntity = CreatePreviewEntity(left.Profile);
            _rightPreviewEntity = CreatePreviewEntity(right.Profile);
            if (_carouselLayoutMode == CarouselLayoutMode.Five && _carouselProfiles.Count >= 4)
            {
                _leftFarPreviewEntity = CreatePreviewEntity(leftFar.Profile);
                _rightFarPreviewEntity = CreatePreviewEntity(rightFar.Profile);
            }

            SetPreview(LeftPreview, LeftNameLabel, left.Profile, _leftPreviewEntity, true);
            SetPreview(RightPreview, RightNameLabel, right.Profile, _rightPreviewEntity, true);
            if (_carouselLayoutMode == CarouselLayoutMode.Five && _carouselProfiles.Count >= 4)
            {
                SetPreview(LeftFarPreview, LeftFarNameLabel, leftFar.Profile, _leftFarPreviewEntity, true);
                SetPreview(RightFarPreview, RightFarNameLabel, rightFar.Profile, _rightFarPreviewEntity, true);
            }
            else
            {
                SetPreview(LeftFarPreview, LeftFarNameLabel, null, default);
                SetPreview(RightFarPreview, RightFarNameLabel, null, default);
            }
            // CCM rework lobby - end
        }

        private void SetPreview(SpriteView view, Label label, ICharacterProfile? profile, EntityUid entity, bool dim = false)
        {
            // CCM rework lobby - start
            if (profile == null || entity == default)
            {
                view.SetEntity((EntityUid?) default);
                label.Text = string.Empty;
                return;
            }

            view.SetEntity((EntityUid?) entity);
            label.Text = profile.Name;
            view.Modulate = dim ? new Color(0.84f, 0.84f, 0.84f, 1f) : Color.White;
            label.Modulate = dim ? new Color(0.84f, 0.84f, 0.84f, 1f) : Color.White;
            // CCM rework lobby - end
        }

        private void RefreshCenterPreviewFromEditor(bool forceRespawn = false)
        {
            // CCM rework lobby - start
            var profile = _profileEditor.Profile;
            if (profile == null || _carouselProfiles.Count == 0)
                return;

            if (_profileEditor.CharacterSlot == null)
                return;

            if (_carouselProfiles[_carouselIndex].Slot != _profileEditor.CharacterSlot.Value)
                return;

            if (_centerPreviewEntity == default)
                return;

            var needsHumanoidRespawn = !_entManager.HasComponent<HumanoidAppearanceComponent>(_centerPreviewEntity);
            if (forceRespawn ||
                needsHumanoidRespawn ||
                profile.Species != _centerPreviewSpeciesId ||
                !_entManager.EntityExists(_centerPreviewEntity))
            {
                if (_centerPreviewEntity != default)
                    _entManager.DeleteEntity(_centerPreviewEntity);

                _centerPreviewEntity = CreatePreviewEntity(profile, _showClothes);
                _centerPreviewSpeciesId = profile.Species;
            }
            else
            {
                _entManager.System<HumanoidAppearanceSystem>().LoadProfile(_centerPreviewEntity, profile);
            }

            SetPreview(CenterPreview, CenterNameLabel, profile, _centerPreviewEntity);
            // CCM rework lobby - end
        }

        private EntityUid CreatePreviewEntity(ICharacterProfile profile, bool showClothes = true)
        {
            // CCM rework lobby - start
            if (profile is not HumanoidCharacterProfile humanoid)
            {
                var defaultSpecies = _protomanager.Index<SpeciesPrototype>(SharedHumanoidAppearanceSystem.DefaultSpecies);
                return _entManager.SpawnEntity(defaultSpecies.DollPrototype, MapCoordinates.Nullspace);
            }

            return UserInterfaceManager.GetUIController<LobbyUIController>()
                .LoadProfileEntity(humanoid, null, showClothes);
            // CCM rework lobby - end
        }

