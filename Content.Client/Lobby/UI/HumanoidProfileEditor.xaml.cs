        private bool HasApplicableMarkings(MarkingCategories category)
        {
            if (Profile == null)
                return false;

            return _markingManager.MarkingsByCategoryAndSpeciesAndSex(category, Profile.Species, Profile.Sex).Count > 0;
        }

        private bool HasAppearanceMarkings()
        {
            if (Profile == null)
                return false;

            foreach (var category in Enum.GetValues<MarkingCategories>())
            {
                if (category is MarkingCategories.Hair or MarkingCategories.FacialHair)
                    continue;

                if (_markingManager.MarkingsByCategoryAndSpeciesAndSex(category, Profile.Species, Profile.Sex).Count > 0)
                    return true;
            }

            return false;
        }

        private bool HasEyeCustomization()
        {
            if (Profile == null)
                return false;

            var speciesProto = _prototypeManager.Index<SpeciesPrototype>(Profile.Species);
            if (!_prototypeManager.TryIndex(speciesProto.SpriteSet, out HumanoidSpeciesBaseSpritesPrototype? baseSprites) ||
                !baseSprites.Sprites.TryGetValue(HumanoidVisualLayers.Eyes, out var eyeSpriteLayerId) ||
                string.IsNullOrWhiteSpace(eyeSpriteLayerId) ||
                !_prototypeManager.TryIndex(eyeSpriteLayerId, out HumanoidSpeciesSpriteLayer? eyeSpriteLayer))
            {
                return false;
            }

            return eyeSpriteLayer.BaseSprite != null;
        }

