        {
            if (!_jobChanceSpinnerTokens.TryGetValue(jobId, out var tokenSource))
                return;

            tokenSource.Cancel();
            tokenSource.Dispose();
            _jobChanceSpinnerTokens.Remove(jobId);
        }

