        }

        private bool OnBeginLeftMenuDrag()
        {
            if (!CenterMenuGlow.Visible || CenterMenuGlow.Parent == null)
                return false;

            _leftMenuDragStarted = true;
            _leftMenuAutoCenter = false;

            if (!_leftMenuAnchorInitialized && CanInitializeLeftMenuAnchor())
                InitializeLeftMenuAnchor(CenterMenuGlow.GlobalPosition);

            var menuGlobal = CenterMenuGlow.GlobalPosition;

            _leftMenuDragOffset = UserInterfaceManager.MousePositionScaled.Position - menuGlobal;
            return true;
        }

        private bool OnContinueLeftMenuDrag(float frameTime)
        {
            if (!_leftMenuDragStarted || CenterMenuGlow.Parent == null)
                return false;

            var parentGlobal = CenterMenuGlow.Parent.GlobalPosition;
            var mousePos = UserInterfaceManager.MousePositionScaled.Position;
            var newPos = mousePos - _leftMenuDragOffset - parentGlobal;
            // CCM rework lobby - start
            var menuSize = CenterMenuGlow.Size;
            var leftGlobal = LeftColumn.GlobalPosition;
            var leftSize = LeftColumn.Size;
            var padding = GetLobbySafePadding();
            if (leftSize.X > 1f && leftSize.Y > 1f && menuSize.X > 1f && menuSize.Y > 1f)
            {
                var minX = leftGlobal.X - parentGlobal.X + padding;
                var maxX = leftGlobal.X - parentGlobal.X + leftSize.X - menuSize.X - padding;
                var minY = leftGlobal.Y - parentGlobal.Y + padding;
                var maxY = leftGlobal.Y - parentGlobal.Y + leftSize.Y - menuSize.Y - padding;
                if (maxX >= minX)
                    newPos.X = Math.Clamp(newPos.X, minX, maxX);
                if (maxY >= minY)
                    newPos.Y = Math.Clamp(newPos.Y, minY, maxY);
            }
            // CCM rework lobby - end
            LayoutContainer.SetPosition(CenterMenuGlow, newPos);
            return true;
        }

        private void OnEndLeftMenuDrag()
        {
            _leftMenuDragStarted = false;
        }

        private void InitializeLeftMenuAnchor(Vector2 menuGlobal)
        {
            if (_leftMenuAnchorInitialized || CenterMenuGlow.Parent == null)
                return;

            var parentGlobal = CenterMenuGlow.Parent.GlobalPosition;
            LayoutContainer.SetAnchorPreset(CenterMenuGlow, LayoutPreset.TopLeft);
            LayoutContainer.SetPosition(CenterMenuGlow, menuGlobal - parentGlobal);
            _leftMenuAnchorInitialized = true;
        }

        private bool CanInitializeLeftMenuAnchor()
        {
            if (CenterMenuGlow.Parent == null)
                return false;

            var parentSize = CenterMenuGlow.Parent.Size;
            var menuSize = CenterMenuGlow.Size;
            return parentSize.X > 1f && parentSize.Y > 1f && menuSize.X > 1f && menuSize.Y > 1f;
        }


