        private void OpenInfoLink(CVarDef<string> cvar)
        {
            var uri = NormalizeAbsoluteHttpUriString(_cfg.GetCVar(cvar));
            if (uri == null)
                return;

            _uriOpener.OpenUri(uri);
        }

        private static string? NormalizeAbsoluteHttpUriString(string? value)
        {
            if (string.IsNullOrWhiteSpace(value))
                return null;

            var trimmed = value.Trim();
            if (!trimmed.StartsWith("http://", StringComparison.OrdinalIgnoreCase) &&
                !trimmed.StartsWith("https://", StringComparison.OrdinalIgnoreCase))
                return null;

            return trimmed;
        }

