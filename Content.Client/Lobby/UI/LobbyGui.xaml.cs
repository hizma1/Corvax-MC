        private void ApplyLobbyUiStyleClasses(string style)
        {
            var oldStyle = style.Equals("old", StringComparison.OrdinalIgnoreCase);
            var showingCharacterSetup = CharacterSetupState.Visible || _oldLayout.CharacterSetupState.Visible;
            ApplyExclusiveStyleClass(RootContainer, oldStyle);
            ApplyExclusiveStyleClass(MainContainer, oldStyle);
            ApplyExclusiveStyleClass(DefaultState, oldStyle);
            ApplyExclusiveStyleClass(CharacterSetupState, oldStyle);
            ApplyExclusiveStyleClass(OldLayoutHost, oldStyle);
            OldLayoutHost.Visible = oldStyle;
            MainContainer.Visible = !oldStyle;
            CharacterSetupState.Visible = !oldStyle && showingCharacterSetup;
            DefaultState.Visible = true;
            RightSide.Visible = true;
            _oldLayout.Visible = oldStyle;
            _oldLayout.CharacterSetupState.Visible = oldStyle && showingCharacterSetup;
            _oldLayout.DefaultState.Visible = true;
            _oldLayout.RightSide.Visible = _oldRightPanelVisible;
            _oldLayout.ExpandPanel.Visible = !_oldRightPanelVisible;
            _oldLayout.Chat.SetLobbyTheme(false, false);
            SetLobbyChatMain(true);
            ApplyLobbyColumnLayout();
            _voteLastSize = Vector2.Zero;

            if (!oldStyle)
            {
                ApplyLobbyTheme(_cfg.GetCVar(RMCCVars.RMCLobbyCrtEnabled));
                RootContainer.InvalidateStyleSheet();
                RootContainer.ForceRunStyleUpdate();
                UpdateResponsiveLayout();
                UpdateVoteContainerAnchor();
                UpdateLeftMenuAnchor();
                UpdateTaskbarAnchor();
            }

            UpdateLobbyLayering();
        }

        private static void ApplyExclusiveStyleClass(Control control, bool oldStyle)
        {
            if (oldStyle)
            {
                control.AddStyleClass(LobbyUiStyleOldClass);
                control.RemoveStyleClass(LobbyUiStyleNewClass);
            }
            else
            {
                control.AddStyleClass(LobbyUiStyleNewClass);
                control.RemoveStyleClass(LobbyUiStyleOldClass);
            }
        }

        private static void SetThemeClass(Control control, bool crtEnabled)
        {
            if (crtEnabled)
            {
                control.AddStyleClass(StyleNano.StyleClassLobbyThemeCrt);
                control.RemoveStyleClass(StyleNano.StyleClassLobbyThemeClean);
            }
            else
            {
                control.AddStyleClass(StyleNano.StyleClassLobbyThemeClean);
                control.RemoveStyleClass(StyleNano.StyleClassLobbyThemeCrt);
            }
        }

        private static void ApplyGlow(Label label, bool crtEnabled)
        {
            if (crtEnabled)
            {
                label.FontColorShadowOverride = StyleNano.LobbyCrtGlow;
                label.ShadowOffsetXOverride = 1;
                label.ShadowOffsetYOverride = 1;
            }
            else
            {
                label.FontColorShadowOverride = null;
                label.ShadowOffsetXOverride = null;
                label.ShadowOffsetYOverride = null;
            }
        }

        private static void ApplyButtonGlow(Button button, bool crtEnabled)
        {
            ApplyGlow(button.Label, crtEnabled);
        }

