        private bool TryGetLobbyStation(out NetEntity station)
        {
            var gameTicker = _entityManager.System<ClientGameTicker>();

            foreach (var entry in gameTicker.StationNames)
            {
                station = entry.Key;
                return true;
            }

            foreach (var entry in gameTicker.JobsAvailable)
            {
                station = entry.Key;
                return true;
            }

            station = default;
            return false;
        }

