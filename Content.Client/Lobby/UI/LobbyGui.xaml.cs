            _oldLayout.LinkDiscordButton.Visible = !linked;
            _oldLayout.LinkDiscordButton.Text = LinkDiscordButton.Text;
        }

        private void ToggleLeftMenu(bool show)
        {
            if (_leftMenuVisible == show)
                return;

            _leftMenuVisible = show;
            CenterMenuGlow.StopAnimation(LeftMenuHideAnimationKey);
            CenterMenuGlow.StopAnimation(LeftMenuShowAnimationKey);

            if (show)
            {
                _leftMenuAutoCenter = true;
                _leftMenuAnchorInitialized = false;
                CenterMenuGlow.Visible = true;
                CenterMenuGlow.Modulate = Color.Transparent;
                CenterMenuGlow.PlayAnimation(BuildLeftMenuAnimation(Color.Transparent, Color.White), LeftMenuShowAnimationKey);
            }
            else
            {
                CenterMenuGlow.PlayAnimation(BuildLeftMenuAnimation(Color.White, Color.Transparent), LeftMenuHideAnimationKey);
            }
        }

        private void OnLeftMenuAnimationCompleted(string key)
        {
            if (key == LeftMenuHideAnimationKey)
            {
                CenterMenuGlow.Visible = false;
                CenterMenuGlow.Modulate = Color.White;
            }
        }

        private static Animation BuildLeftMenuAnimation(Color from, Color to)
        {
            return new Animation
            {
                Length = TimeSpan.FromSeconds(0.15f),
                AnimationTracks =
                {
                    new AnimationTrackControlProperty
                    {
                        Property = nameof(Control.Modulate),
                        InterpolationMode = AnimationInterpolationMode.Linear,
                        KeyFrames =
                        {
                            new AnimationTrackProperty.KeyFrame(from, 0f),
                            new AnimationTrackProperty.KeyFrame(to, 0.15f)
                        }
                    }
                }
            };
        }

        protected override void FrameUpdate(FrameEventArgs args)
        {
            base.FrameUpdate(args);
            _leftMenuDragHelper.Update(args.DeltaSeconds);

            if (IsOldLayoutActive)
            {
                if (_oldLayout.VoteContainer.Size.X > 1f && _oldLayout.VoteContainer.Size.Y > 1f)
                    _voteLastSize = _oldLayout.VoteContainer.Size;
                UpdateOldHeaderWidths();
                return;
            }

            UpdateResponsiveLayout();

            if (CenterMenuGlow.Visible)
                _leftMenuLastGlobal = CenterMenuGlow.GlobalPosition;

            // CCM rework lobby - start
            if (VoteContainer.Size.X > 1f && VoteContainer.Size.Y > 1f)
                _voteLastSize = VoteContainer.Size;
            // CCM rework lobby - end

            UpdateVoteContainerAnchor();
            UpdateLeftMenuAnchor();
            UpdateTaskbarAnchor();
        }

        private void UpdateResponsiveLayout()
        {
            var mainSize = MainContainer.Size;
            var leftSize = LeftColumn.Size;
            if (mainSize.X <= 1f || mainSize.Y <= 1f || leftSize.X <= 1f || leftSize.Y <= 1f)
                return;

            if (Math.Abs(mainSize.X - _lastResponsiveMainSize.X) < 1f &&
                Math.Abs(mainSize.Y - _lastResponsiveMainSize.Y) < 1f &&
                Math.Abs(leftSize.X - _lastResponsiveLeftSize.X) < 1f &&
                Math.Abs(leftSize.Y - _lastResponsiveLeftSize.Y) < 1f)
            {
                return;
            }

            _lastResponsiveMainSize = mainSize;
            _lastResponsiveLeftSize = leftSize;

            _compactLobbyLayout = mainSize.Y <= 820f;
            var shortHeightLayout = mainSize.Y <= 920f;

            var centerFactor = _compactLobbyLayout
                ? 0.36f
                : shortHeightLayout
                    ? 0.34f
                    : 0.32f;
            var centerWidth = Math.Clamp(leftSize.X * centerFactor, LobbyCenterWidthMin, LobbyCenterWidthMax);
            CenterMenuGlow.SetWidth = centerWidth;
            CenterMenuPanel.SetWidth = centerWidth;
            CenterMenuGlow.MinSize = new Vector2(centerWidth, CenterMenuGlow.MinSize.Y);
            CenterMenuPanel.MinSize = new Vector2(centerWidth, CenterMenuPanel.MinSize.Y);

            var taskbarFactor = _compactLobbyLayout
                ? 0.72f
                : shortHeightLayout
                    ? 0.68f
                    : 0.64f;
            var taskbarWidth = Math.Clamp(leftSize.X * taskbarFactor, LobbyTaskbarWidthMin, LobbyTaskbarWidthMax);
            var taskbarHeight = _compactLobbyLayout ? LobbyTaskbarHeightCompact : LobbyTaskbarHeightDefault;
            LeftTaskbarPanel.SetWidth = taskbarWidth;
            LeftTaskbarPanel.SetHeight = taskbarHeight;
            LeftTaskbarPanel.MinSize = new Vector2(taskbarWidth, taskbarHeight);
            LeftTaskbarPanel.MaxSize = new Vector2(taskbarWidth, taskbarHeight);

            MainContainer.SeparationOverride = _compactLobbyLayout ? 6 : 8;
            TopButtonBar.SeparationOverride = _compactLobbyLayout ? 4 : 6;

            var topButtonHeight = _compactLobbyLayout ? 36f : 40f;
            LinkDiscordButton.MinSize = new Vector2(0f, topButtonHeight);
            AHelpButton.MinSize = new Vector2(0f, topButtonHeight);
            UpdatesButton.MinSize = new Vector2(0f, topButtonHeight);
            GuidesButton.MinSize = new Vector2(0f, topButtonHeight);
            PollsButton.MinSize = new Vector2(0f, topButtonHeight);
            LeaveButton.MinSize = new Vector2(0f, topButtonHeight);
            TopButtonBar.MinSize = new Vector2(0f, topButtonHeight);

            TaskbarRatingButton.MinSize = _compactLobbyLayout ? new Vector2(54f, 40f) : new Vector2(64f, 48f);
            TaskbarDonateButton.MinSize = _compactLobbyLayout ? new Vector2(96f, 48f) : new Vector2(108f, 56f);
            TaskbarMenuButton.MinSize = _compactLobbyLayout ? new Vector2(76f, 40f) : new Vector2(86f, 48f);
            TaskbarCustomizationButton.MinSize = _compactLobbyLayout ? new Vector2(96f, 48f) : new Vector2(108f, 56f);
            TaskbarAchievementsButton.MinSize = _compactLobbyLayout ? new Vector2(54f, 40f) : new Vector2(64f, 48f);
        }

        private float GetLobbySafePadding()
        {
            return _compactLobbyLayout ? LobbyEdgePaddingCompact : LobbyEdgePaddingDefault;
        }

        private void UpdateLeftMenuAnchor()
        {
            if (_leftMenuDragStarted || CenterMenuGlow.Parent == null)
                return;

            if (!_leftMenuAutoCenter)
                return;

            var menuSize = CenterMenuGlow.Size;
            var leftSize = LeftColumn.Size;
            if (menuSize.X <= 1f || menuSize.Y <= 1f || leftSize.X <= 1f || leftSize.Y <= 1f)
                return;

            var parentGlobal = CenterMenuGlow.Parent.GlobalPosition;
            var leftGlobal = LeftColumn.GlobalPosition;
            var padding = GetLobbySafePadding();

            var x = leftGlobal.X - parentGlobal.X + (leftSize.X - menuSize.X) * 0.5f;
            var y = leftGlobal.Y - parentGlobal.Y + (leftSize.Y - menuSize.Y) * 0.5f + leftSize.Y * LeftMenuCenterYOffsetPercent;

            var minX = leftGlobal.X - parentGlobal.X + padding;
            var maxX = leftGlobal.X - parentGlobal.X + leftSize.X - menuSize.X - padding;
            var minY = leftGlobal.Y - parentGlobal.Y + padding;
            var maxY = leftGlobal.Y - parentGlobal.Y + leftSize.Y - menuSize.Y - padding;
            if (maxX >= minX)
                x = Math.Clamp(x, minX, maxX);
            if (maxY >= minY)
                y = Math.Clamp(y, minY, maxY);

            LayoutContainer.SetAnchorPreset(CenterMenuGlow, LayoutPreset.TopLeft);
            LayoutContainer.SetPosition(CenterMenuGlow, new Vector2(x, y));
            _leftMenuAnchorInitialized = true;
        }

        private void UpdateVoteContainerAnchor()
        {
            if (VoteContainer.Parent == null)
                return;

            var voteSize = VoteContainer.Size;
            if (voteSize.X <= 1f || voteSize.Y <= 1f)
                voteSize = _voteLastSize;
            if (voteSize.X <= 1f || voteSize.Y <= 1f)
                return;

            var parentSize = VoteContainer.Parent.Size;
            if (parentSize.X <= 1f || parentSize.Y <= 1f)
                return;

            var padding = GetLobbySafePadding();
            var voteX = (parentSize.X - voteSize.X) * 0.5f;
            var voteY = _compactLobbyLayout ? 4f : 6f;

            var minX = padding;
            var maxX = parentSize.X - voteSize.X - padding;
            if (maxX >= minX)
                voteX = Math.Clamp(voteX, minX, maxX);

            LayoutContainer.SetAnchorPreset(VoteContainer, LayoutPreset.TopLeft);
            LayoutContainer.SetPosition(VoteContainer, new Vector2(voteX, voteY));
        }

        private void UpdateOldVoteContainerAnchor()
        {
            if (_oldLayout.VoteContainer.Parent == null)
                return;

            var voteSize = _oldLayout.VoteContainer.Size;
            if (voteSize.X <= 1f || voteSize.Y <= 1f)
                voteSize = _voteLastSize;
            if (voteSize.X <= 1f || voteSize.Y <= 1f)
                return;

            var parentSize = _oldLayout.VoteContainer.Parent.Size;
            if (parentSize.X <= 1f || parentSize.Y <= 1f)
                return;

            var padding = 8f;
            var voteX = padding;
            var voteY = 8f;
            var maxX = MathF.Max(padding, parentSize.X - voteSize.X - padding);
            voteX = Math.Clamp(voteX, padding, maxX);

            LayoutContainer.SetAnchorPreset(_oldLayout.VoteContainer, LayoutPreset.TopLeft);
            LayoutContainer.SetPosition(_oldLayout.VoteContainer, new Vector2(voteX, voteY));
        }

        private void UpdateTaskbarAnchor()
        {
            if (LeftTaskbarPanel.Parent == null)
                return;

            var panelSize = LeftTaskbarPanel.Size;
            if (panelSize.X <= 1f || panelSize.Y <= 1f)
                return;

            var parentGlobal = LeftTaskbarPanel.Parent.GlobalPosition;
            var leftGlobal = LeftColumn.GlobalPosition;
            var leftSize = LeftColumn.Size;
            if (leftSize.X <= 1f || leftSize.Y <= 1f)
                return;

            var padding = GetLobbySafePadding();
            var x = leftGlobal.X - parentGlobal.X + (leftSize.X - panelSize.X) * 0.5f;
            var y = leftGlobal.Y - parentGlobal.Y + leftSize.Y - panelSize.Y - padding;

            var minX = leftGlobal.X - parentGlobal.X + padding;
            var maxX = leftGlobal.X - parentGlobal.X + leftSize.X - panelSize.X - padding;
            if (maxX >= minX)
                x = Math.Clamp(x, minX, maxX);

            LayoutContainer.SetAnchorPreset(LeftTaskbarPanel, LayoutPreset.TopLeft);
            LayoutContainer.SetPosition(LeftTaskbarPanel, new Vector2(x, y));
        }

        private void OnLeftMenuDragKeyBindDown(GUIBoundKeyEventArgs args)
        {
