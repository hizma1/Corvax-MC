        public void SwitchState(LobbyGuiState state)
        {
            CharacterSetupState.Visible = false;
            _oldLayout.CharacterSetupState.Visible = false;
            UpdateLobbyLayering();

            switch (state)
            {
                case LobbyGuiState.Default:
                    DefaultState.Visible = true;
                    RightSide.Visible = true;
                    CharacterSetupState.Visible = false;
                    _oldLayout.DefaultState.Visible = true;
                    _oldLayout.RightSide.Visible = _oldRightPanelVisible;
                    _oldLayout.ExpandPanel.Visible = !_oldRightPanelVisible;
                    _oldLayout.CharacterSetupState.Visible = false;
                    break;
                case LobbyGuiState.CharacterSetup:
                    DefaultState.Visible = true;
                    CharacterSetupState.Visible = !IsOldLayoutActive;
                    RightSide.Visible = true;
                    _oldLayout.DefaultState.Visible = true;
                    _oldLayout.CharacterSetupState.Visible = IsOldLayoutActive;
                    _oldLayout.RightSide.Visible = _oldRightPanelVisible;
                    _oldLayout.ExpandPanel.Visible = !_oldRightPanelVisible;

                    UserInterfaceManager.GetUIController<LobbyUIController>().ReloadCharacterSetup();

                    break;
            }
        }

